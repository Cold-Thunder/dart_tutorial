/// Polymorphism:
/// 2 Types of Polymorphism:
///   1. Compile Time polymorphism = (overloading)
///   2. Run time polymorphism = (overriding)
///
/// Polymorphism in Dart (or any programming language) is
/// a fundamental concept in object-oriented programming (OOP) that allows
/// a single interface to represent different underlying forms (data types or classes).
/// It enables the ability to write flexible and reusable code.
///
/// 1. Subclasses can override the behavior of the parent class.
/// 2. It allows us to write code that is more flexible and reusable.
///
///
/// There are 2 types of polymorphism:
/// 1. Compile time polymorphism => dart doesn't support.
/// 2. Run time Polymorphism => This occurs when the method to be executed
///                 is determined at runtime.
///                 In Dart, this is implemented using method overriding in inheritance.