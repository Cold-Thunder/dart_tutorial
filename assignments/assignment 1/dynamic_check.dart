void main(){
  //assigning String value
  dynamic name = "Hemal";
  //reassigning Integer value
  name = 10;
  print("Integer value: $name");

  // applying arithmetic operation
  name += 10;
  print("Addition: $name");

  name -= 10;
  print("Subtract: $name");

  name *= 10;
  print("Multiplication: $name");

  name /= 10;
  print("Division: $name");

  //reassigning boolean value in it
  name = true;
  print("Boolean value: $name");
}