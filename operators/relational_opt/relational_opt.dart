///Relational Operators or Comparison Operators is mainly used for comparing between 2 expressions.
///
///Relational operators:
///
/// > (Greater Than) | a > b
/// < (Less Than) | a < b
/// >= (Greater than or equal) | a >= b
/// <= (Less than or equal) | a <= b
/// == (is equal to) | a == b
/// != (not equal) | a != b

void main() {
  int a = 10;
  int b = 5;

  //comparisons
  // use of (<)greater than
  a > b ? print("true") : print("false");
  // use of (>) less than
  a < b ? print("true") : print("false");
  // use of (>=) greater than or equal
  a >= b ? print("true") : print("false");
  // use of (<=) less than or equal
  a <= b ? print("true") : print("false");
  // use of (!=) not equal
  a != b ? print("true") : print("false");
}
