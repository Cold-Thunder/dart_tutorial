///When the number of execution of a block is not known,
///it depends on the condition true
///while loop is another looping statement in dart
///basic syntax of while loop in dart
///
///while(condition){
///   statement
///   increment/decrement
///   }
void main(){
  int i =0;
  while(i <= 10){
    print(i);
    i++;
  }
}